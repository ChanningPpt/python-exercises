# Seeing The World Exercise

places = ['bangkok', 'paris', 'london', 'dubai', 'nyc']

# Show original list
print(places)

# Show sorted list
print(sorted(places))

print(places)

print(sorted(places, reverse=True))

print(places)

places.reverse()
print(places)
places.reverse()
print(places)

places.sort()
print(places)
places.sort(reverse=True)
print(places)
