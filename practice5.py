# Pizza and Animals Exercise

pizzas = ['cheese', 'pepperoni', 'hawaiian']
for pizza in pizzas:
    print(f"{pizza.title()} pizza tastes good.")
print("\nPizza tastes pretty good overall.\n")

animals = ['lions', 'tigers', 'cougars']
for animal in animals:
    print(f"{animal.title()} are a type of cat")
print("\nThese cats are very aggressive predators and can kill you.")
